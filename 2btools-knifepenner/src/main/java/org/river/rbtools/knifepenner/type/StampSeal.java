package org.river.rbtools.knifepenner.type;

import java.io.Serializable;

public class StampSeal implements Serializable{
	private static final long serialVersionUID = -5892503118151279467L;

	private String id;
	
	private String sealName;
	
	private int x;
	
	private int y;
	
	private int width;
	
	private int height;

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSealName() {
		return sealName;
	}

	public void setSealName(String sealName) {
		this.sealName = sealName;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}	
}
